print('Задача 2. Финансовый отчёт')

first_q = int(input('Введите сумму дохода за первый квартал: '))
second_q = int(input('Введите сумму дохода за второй квартал: '))
third_q = int(input('Введите сумму дохода за третий квартал: '))
